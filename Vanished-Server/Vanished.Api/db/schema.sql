    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    last_seen_at TIMESTAMPTZ NULL,
    revoked_at TIMESTAMPTZ NULL
);

CREATE INDEX IF NOT EXISTS ix_devices_user_id ON devices (user_id);
CREATE INDEX IF NOT EXISTS ix_devices_device_id ON devices (device_id);
CREATE INDEX IF NOT EXISTS ix_devices_user_revoked ON devices (user_id, revoked_at);
CREATE INDEX IF NOT EXISTS ix_devices_active_by_user ON devices (user_id, revoked_at, device_id);
CREATE UNIQUE INDEX IF NOT EXISTS ux_devices_user_device_id ON devices (user_id, device_id);

