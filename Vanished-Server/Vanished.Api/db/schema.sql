-- Vanished API PostgreSQL baseline schema.
-- Development mode: this file represents the current canonical schema.
-- No version table, version inserts or ALTER-based historical patches are kept.
-- Zero-Knowledge contract: stores only public keys, verifier/hash material,
-- encrypted envelopes and token hashes. It must never store plaintext passwords,
-- private keys, master keys, recovery keys or plaintext messages.

CREATE TABLE IF NOT EXISTS users (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    username VARCHAR(50) NOT NULL,
    full_name VARCHAR(100) NOT NULL DEFAULT '',
    email VARCHAR(254) NOT NULL,
    identity_public_key TEXT NOT NULL,
    key_version INTEGER NOT NULL DEFAULT 1,
    recovery_key_hash VARCHAR(128) NOT NULL,
    avatar_blob BYTEA NULL,
    avatar_mime VARCHAR(100) NULL,
    bio VARCHAR(160) NOT NULL DEFAULT '',
    mfa_totp_enabled BOOLEAN NOT NULL DEFAULT FALSE,
    account_pin_hash VARCHAR(128) NULL,
    account_pin_salt VARCHAR(64) NULL,
    account_pin_failed_attempts INTEGER NOT NULL DEFAULT 0,
    account_pin_locked_until TIMESTAMPTZ NULL,
    account_pin_updated_at TIMESTAMPTZ NULL,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    last_seen_at TIMESTAMPTZ NULL
);

CREATE UNIQUE INDEX IF NOT EXISTS ux_users_username ON users (username);
CREATE UNIQUE INDEX IF NOT EXISTS ux_users_email ON users (email);
CREATE UNIQUE INDEX IF NOT EXISTS ux_users_email_lower_unique ON users (LOWER(email));
CREATE UNIQUE INDEX IF NOT EXISTS ux_users_recovery_key_hash ON users (recovery_key_hash);
CREATE INDEX IF NOT EXISTS ix_users_username ON users (username);
CREATE INDEX IF NOT EXISTS ix_users_email ON users (email);
CREATE INDEX IF NOT EXISTS ix_users_email_lower ON users (LOWER(email));
CREATE INDEX IF NOT EXISTS ix_users_username_lower ON users (LOWER(username));
CREATE INDEX IF NOT EXISTS ix_users_account_pin_lock ON users (account_pin_locked_until)
    WHERE account_pin_locked_until IS NOT NULL;
CREATE INDEX IF NOT EXISTS ix_users_pin_configured ON users (id)
    WHERE account_pin_hash IS NOT NULL;

-- Drop legacy columns that are no longer consumed by the API/client.
ALTER TABLE users DROP COLUMN IF EXISTS totp_secret_ciphertext;

CREATE TABLE IF NOT EXISTS devices (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id INTEGER NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    device_id VARCHAR(64) NOT NULL,
    public_key TEXT NOT NULL,
