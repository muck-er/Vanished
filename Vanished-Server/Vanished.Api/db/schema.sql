CREATE TABLE IF NOT EXISTS message_threads (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_low_id INTEGER NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    user_high_id INTEGER NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    initiator_id INTEGER NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    recipient_id INTEGER NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    status VARCHAR(20) NOT NULL DEFAULT 'pending',
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    accepted_at TIMESTAMPTZ NULL,
    rejected_at TIMESTAMPTZ NULL,
    CONSTRAINT ck_message_threads_status CHECK (status IN ('pending','accepted','rejected')),
    CONSTRAINT ck_message_threads_pair CHECK (user_low_id < user_high_id)
);

CREATE UNIQUE INDEX IF NOT EXISTS ux_message_threads_pair ON message_threads (user_low_id, user_high_id);
CREATE INDEX IF NOT EXISTS ix_message_threads_recipient_status ON message_threads (recipient_id, status);
CREATE INDEX IF NOT EXISTS ix_message_threads_initiator_status ON message_threads (initiator_id, status);
CREATE INDEX IF NOT EXISTS idx_conversations_user_low_updated ON message_threads (user_low_id, updated_at DESC);
CREATE INDEX IF NOT EXISTS idx_conversations_user_high_updated ON message_threads (user_high_id, updated_at DESC);
CREATE INDEX IF NOT EXISTS idx_message_requests_recipient ON message_threads (recipient_id, status, created_at DESC);

